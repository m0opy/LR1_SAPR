task apb_write(input [31:0] addr, input [31:0] data); // ?????????? ?????? ?? APB
  wait ((penable==0) && (pready == 0)); // ???????? ???????????? ????(penable==0) ? ??????????(pready == 0)
  @(posedge pclk); 		// ????
  psel <= 1'b1; 		// ????? ??????????
  paddr[31:0] <= addr[31:0]; 	// ?????????? ?????
  pwdata[31:0] <= data[31:0]; 	// ?????????? ?????? ?? ??????
  pwrite <= 1'b1; 		// ???????? ??????
 
  @(posedge pclk);		// ????
  penable <= 1'b1;		// ?????????? ??????????

  @(posedge pclk);		// ????
  wait (pready == 1'b1);	// ???? ?????????? ??????????

  @(posedge pclk);		// ????
  psel <= 1'b0;			// ??????????? ??????????
  penable <= 1'b0;		// ?????????? ?????????????
  pwrite <= 1'b0;		// ????????? ????????

  @(posedge pclk);		// ????
endtask


task apb_read(input [31:0] addr, output logic [31:0] data); // ?????????? ?????? ?? APB
  wait ((penable==0) && (pready == 0)); // ???????? ???????????? ????(penable==0) ? ??????????(pready == 0)

  @(posedge pclk); 		// ????
