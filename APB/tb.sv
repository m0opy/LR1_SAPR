  paddr[31:0] <= addr[31:0]; 	// ??????????? ??????

  @(posedge pclk);		// ????
  penable <= 1'b1;		// ?????????? ??????????

  @(posedge pclk);		// ????
  wait (pready == 1'b1);	// ???? ?????????? ??????????
  data[31:0] = prdata[31:0];	// ?????? ??????

  @(posedge pclk);		// ????
  psel <= 1'b0;			// ???????????? ??????????
  penable <= 1'b0;		// ????? ??????????

  @(posedge pclk);		// ????
endtask

